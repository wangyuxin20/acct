package com.wa.apiaccount.core.model;

public class Mkt {
	String mktSeq = null;
	String amt = null;
	String userNo = null;
	String note = null;
	String number = null;
	
	public String getMktSeq() {
		return mktSeq;
	}
	public void setMktSeq(String mktSeq) {
		this.mktSeq = mktSeq;
	}
	public String getAmt() {
		return amt;
	}
	public void setAmt(String amt) {
		this.amt = amt;
	}
	public String getUserNo() {
		return userNo;
	}
	public void setUserNo(String userNo) {
		this.userNo = userNo;
	}
	public String getNote() {
		return note;
	}
	public void setNote(String note) {
		this.note = note;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	
}
